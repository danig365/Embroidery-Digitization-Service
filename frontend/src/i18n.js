const getDomainLanguage = () => {
  if (typeof window === "undefined") return "en";

  const host = window.location.hostname.toLowerCase();

  if (host === "broderiemagique.com" || host === "www.broderiemagique.com") {
    return "fr";
  }

  return "en";
};

const getDomainTitle = () => {
  if (typeof window === "undefined") return "AI Embroidery Files";

  const host = window.location.hostname.toLowerCase();

  if (host === "broderiemagique.com" || host === "www.broderiemagique.com") {
    return "Broderie Magique";
  }

  return "AI Embroidery Files";
};

const storedLanguage = typeof window !== "undefined"
  ? localStorage.getItem("app_language")
  : null;

const initialLanguage = storedLanguage || getDomainLanguage();

i18n.use(initReactI18next).init({
  resources,
  lng: initialLanguage,
  fallbackLng: "en",
  interpolation: {
    escapeValue: false
  }
});

if (typeof window !== "undefined") {
  document.documentElement.lang = i18n.language;
  document.title = getDomainTitle();
  i18n.on("languageChanged", (lng) => {
    document.documentElement.lang = lng;
    document.title = getDomainTitle();
    localStorage.setItem("app_language", lng);
  });
}

